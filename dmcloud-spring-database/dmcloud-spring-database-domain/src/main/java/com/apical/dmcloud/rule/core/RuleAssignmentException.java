package com.apical.dmcloud.rule.core;

public class RuleAssignmentException extends RuleException {

	private static final long serialVersionUID = 1L;
	
	public RuleAssignmentException() {
	}

	public RuleAssignmentException(String message) {
		super(message);
	}

	public RuleAssignmentException(Throwable cause) {
		super(cause);
	}

	public RuleAssignmentException(String message, Throwable cause) {
		super(message, cause);
	}

}
